async function seedWorkshops() {
  try {
    // Check if workshops already exist
    const existingCount = await Workshop.countDocuments();
    if (existingCount > 0) {
      console.log(`Workshops already seeded. Found ${existingCount} existing workshops.`);
      return;
    }

    // Insert all workshops
    const result = await Workshop.insertMany(DUMMY_WORKSHOPS);
    console.log(`Successfully seeded ${result.length} workshops to the database.`);
    return result;
  } catch (error) {
    console.error('Error seeding workshops:', error);
    throw error;
  }
}

